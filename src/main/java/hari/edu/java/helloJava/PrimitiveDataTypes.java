public class PrimitiveDataTypes {

    public static void main(String args[]){
        //Byte: 8 bits (-127 to 127)
        byte byteVariable;
        byteVariable=127;
        System.out.println("Primitive data type -> BYTE :  "+ byteVariable);
        //Short: 16 bits (-32768 to 32767)
        short shortVariable;
        shortVariable=32767;
        System.out.println("Primitive data type -> SHORT :  "+ shortVariable);
        //Int: 32 bits
        int intVariable;
        intVariable=2;
        System.out.println("Primitive data type -> INT :  "+ intVariable);
        //Long: 64 Bit
        //Float: 32 bit (Floating point types)
        //Double: 64 Bit (Floating point types)
        //Boolean: True or False
        //Char: 16 Bit (Contains Unicode Characters)
        char charVariable;
        charVariable='A';
        System.out.println("Primitive data type -> CHAR :  "+ charVariable);
    }
}
